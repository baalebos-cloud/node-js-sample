// A simple Java program that greets the user and does a quick calculation
public class Main {
    public static void main(String[] args) {
        // Print a greeting
        System.out.println("Hello, welcome to your first Java project!");

        // Do a small calculation
        int a = 5;
        int b = 7;
        int sum = a + b;

        System.out.println("Let's add two numbers: " + a + " + " + b + " = " + sum);

        // Show how to loop
        System.out.println("Counting from 1 to 5:");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
}
